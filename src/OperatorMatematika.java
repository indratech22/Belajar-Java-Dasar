public class OperatorMatematika {

    public static void main(String[] args) {

        int a = 100;
        int b =10;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);


        int c = 10;

        c+=10;
        System.out.println(c);
        c-=5;
        System.out.println(c);
        c%=2;
        System.out.println(c);

        int d = 100;
        int e = -50;

        d++;
        System.out.println(d);
        e--;
        System.out.println(e);
        System.out.println(!true);
    }

}
